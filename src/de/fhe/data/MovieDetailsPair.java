package de.fhe.data;

import java.io.Serializable;

public class MovieDetailsPair implements Serializable
{
	private static final long serialVersionUID = 105454271881470337L;
	
	private String first = null;
	private String second = null;
	
	public MovieDetailsPair(String first, String second)
	{
		this.first = first;
		this.second = second;
	}
	
	public MovieDetailsPair()
	{
	}
	
	public String getFirst()
	{
		return first;
	}

	public void setFirst(String first)
	{
		this.first = first;
	}
	
	public String getSecond()
	{
		return second;
	}

	public void setSecond(String second)
	{
		this.second = second;
	}
}
